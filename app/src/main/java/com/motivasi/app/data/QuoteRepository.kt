    fun getQuoteById(id: Int): Quote? {
        return quotes.find { it.id == id }
    }

    fun getQuoteForDate(dayOfYear: Int): Quote {
        // dayOfYear is 1-365 (or 366)
        // Use modulo to cycle through quotes if we have fewer than 366, but now we have enough!
        val index = (dayOfYear - 1) % quotes.size
        return quotes[index]
    }

    fun getRandomQuote(): Quote {
        return quotes.random()
    }
    
    fun getCategories(): List<String> {
        return quotes.map { it.category }.distinct()
    }
    
    fun getQuotesByCategory(category: String): List<Quote> {
        return quotes.filter { it.category == category }
    }
    
    fun getQuoteCount(): Int = quotes.size
